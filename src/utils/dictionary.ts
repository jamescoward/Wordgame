/**
 * Returns true if the given word (case-insensitive) exists in the dictionary.
 */
export function isValidWord(word: string): boolean {
  return VALID_WORDS.has(word.toUpperCase())
}

/**
 * Returns true if `word` can be formed from `availableLetters` (handles duplicates).
 */
export function canFormWord(word: string, availableLetters: string[]): boolean {
  const pool = [...availableLetters.map(l => l.toUpperCase())]
  for (const ch of word.toUpperCase()) {
    const idx = pool.indexOf(ch)
    if (idx === -1) return false
    pool.splice(idx, 1)
  }
  return true
}
